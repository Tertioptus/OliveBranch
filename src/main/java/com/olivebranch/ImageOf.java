package com.olivebranch;

/**
 * ImageOf is an enumerator that indicates a type of rendered image of a GUI.
 * 
 * @author Benjamin Paige
 * @version 1.0
 * @version 1.0
 *
 */
public enum ImageOf {

	/**
	 * Screenshot of GUI in current state
	 */
	SCREEN,
	
	/**
	 * Architectural map, likely XML, of GUI's structure
	 */
	MARKUP;
}
